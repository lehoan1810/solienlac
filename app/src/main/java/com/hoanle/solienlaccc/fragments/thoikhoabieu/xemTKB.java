package com.hoanle.solienlaccc.fragments.thoikhoabieu;

public class xemTKB {
    private String Thu;
    private int Hinh;

    public xemTKB(String thu, int hinh) {
        Thu = thu;
        Hinh = hinh;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }

    public String getThu() {
        return Thu;
    }

    public void setThu(String thu) {
        Thu = thu;
    }
}
